Strip              *
CreateStrip(char *name)
{
   EDBUG(5, "CreateStrip");
   name = NULL;
   EDBUG_RETURN(NULL);
}

void
DestroyStrip(Strip * s)
{
   EDBUG(5, "DestroyStrip");
   s = NULL;
   EDBUG_RETURN_;
}

